import pandas as pd
print("========================================================================================")

s1 = pd.Series(['a', 'b'])
print(s1)

s2 = pd.Series(['c', 'd'])
print(s2)

print("========================================================================================")
print(pd.concat([s1, s2], ignore_index=True))    #index 0123


print("========================================================================================")
print(pd.concat([s1, s2], keys=['s1', 's2'],
                names=['Series name', 'Row ID']))


print("========================================================================================")
df1 = pd.DataFrame([['a', 1], ['b', 2]],
                   columns=['letter', 'number'])
print(df1)

print("========================================================================================")
df3 = pd.DataFrame([['c', 3, 'cat'], ['d', 4, 'dog']],
                   columns=['letter', 'number', 'animal'])
print(df3)

print("========================================================================================")
print(pd.concat([df1, df3]))   # confuse tables

print("========================================================================================")
print(pd.concat([df1, df3], join="inner")) #animal column deleted


print("========================================================================================")
df4 = pd.DataFrame([['bird', 'polly'], ['monkey', 'george']],
                   columns=['animal', 'name'])
print(df4)


print("========================================================================================")
df5 = pd.DataFrame([1], index=['a'])
df6 = pd.DataFrame([2], index=['a'])

print(df5)
print(df6)

print("========================================================================================")
print(pd.concat([df5, df6]))  #verify_integrity=True는 중복되는 인덱스 찾아낼때 쓰임









